<script type="text/javascript">
    $(function(){
        var exampleSwiper = new Swiper('#example', {
            autoplay: 5000,
            loop: true,
            pagination: {
                el: '#example .pagination',
            }
        });

        $(".swiper-pagination-bullet").click(function(){
            $(this).addClass("swiper-pagination-bullet-active").siblings().removeClass("swiper-pagination-bullet-active")
            var index = $(this).index()+1;
            exampleSwiper.slideTo(index, 1000, false);
        })

        $(".question").click(function(){
            $(this).find(".question-text").toggle();
            if( $(this).find(".question-text").is(":hidden") ){
                $(this).find(".question-tit").removeClass("question-tit-yes")
            }else{
                $(this).find(".question-tit").addClass("question-tit-yes")
            }
        })

    })
</script>
